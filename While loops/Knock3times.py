def knock_three_times():            #define a function calle knock three times
    count=0                         #asign to count a value initiate
    while count< 3:                 #cicle while , where condition is count be less than 3
        print("Knock Knock")        #print a message "knock knock"
        count+=1                    #asign and add 1 to count


knock_three_times()